CREATE TABLE PRODUCTO(
	CODIGO_PRODUCTO INT IDENTITY ,
	NOMBRE_PRODUCTO VARCHAR(50) NOT NULL,
	CATEGORIA VARCHAR(50) NOT NULL,
	MARCA VARCHAR(50) NOT NULL,
	PRECIO_PRODUCTO FLOAT NOT NULL,
	STOCK INT,
	ID_DEPOSITO INT NOT NULL,

	CONSTRAINT PK_CODIGO_PRODUCTO PRIMARY KEY (CODIGO_PRODUCTO),
	CONSTRAINT FK_ID_DEPOSITO FOREIGN KEY (ID_DEPOSITO) REFERENCES DEPOSITO(ID_DEPOSITO),
	CONSTRAINT CHK_CODIGO_PRODUCTO CHECK (CODIGO_PRODUCTO >= 0),
	CONSTRAINT CHK_NOMBRE_PRODUCTO CHECK (NOMBRE_PRODUCTO LIKE '%[a-zA-Z]%'),
	CONSTRAINT CHK_CATEGORIA CHECK (CATEGORIA IN ('Juegos', 'Tecnologia', 'Limpieza', 'Muebles Interior', 'Muebles Exterior')),
	CONSTRAINT CHK_MARCA CHECK (NOMBRE_PRODUCTO LIKE '%[a-zA-Z]%'),
	CONSTRAINT CHK_PRECIO_PRODUCTO CHECK (PRECIO_PRODUCTO >= 0),
	CONSTRAINT CHK_STOCK CHECK (STOCK >= 0)
);

CREATE TABLE PROVEEDOR(
	CUIT_PROVEEDOR BIGINT,
	NOMBRE_PROVEEDOR VARCHAR(50) NOT NULL,
	CODIGO_PRODUCTO INT,	
	PROVINCIA VARCHAR(50) NOT NULL

	CONSTRAINT PK_CUIT_PROVEEDOR PRIMARY KEY (CUIT_PROVEEDOR),
	CONSTRAINT FK_CODIGO_PRODUCTO_PROVEEDOR FOREIGN KEY (CODIGO_PRODUCTO) REFERENCES PRODUCTO(CODIGO_PRODUCTO),
	CONSTRAINT CHK_CUIT_PROVEEDOR CHECK (LEN(CUIT_PROVEEDOR) = 11),
	CONSTRAINT CHK_NOMBRE_PROVEEDOR CHECK (NOMBRE_PROVEEDOR LIKE '%[a-zA-Z]%'),
	CONSTRAINT CHK_PROVINCIA_PROVEEDOR CHECK (PROVINCIA LIKE '%[a-zA-Z]%')
	
);

CREATE TABLE EMPLEADO(
	CUIT_EMPLEADO BIGINT UNIQUE,
	NOMBRE_EMPLEADO VARCHAR(50) NOT NULL,
	FECHA_NACIMIENTO_EMPLEADO DATE NOT NULL,
	PUESTO VARCHAR(50) NOT NULL,
	DIRECCION VARCHAR(50) NOT NULL,
	SALARIO FLOAT NOT NULL

	CONSTRAINT PK_ID_EMPLEADO PRIMARY KEY (CUIT_EMPLEADO),
	CONSTRAINT CHK_CUIT_EMPLEADO CHECK (LEN(CUIT_EMPLEADO) = 11),
	CONSTRAINT CHK_NOMBRE_EMPLEADO CHECK (NOMBRE_EMPLEADO LIKE '%[a-zA-Z]%'),
	CONSTRAINT CHK_PUESTO CHECK (PUESTO LIKE '%[a-zA-Z]%'),
	CONSTRAINT CHK_SALARIO CHECK (SALARIO >= 0)
);

CREATE TABLE CLIENTE(
	DNI_CLIENTE INT UNIQUE,
	NOMBRE_CLIENTE VARCHAR(50) NOT NULL,
	MEMBRESIA VARCHAR(50) NOT NULL,
	FECHA_NACIMIENTO_CLIENTE DATE NOT NULL

	CONSTRAINT PK_DNI_CLIENTE PRIMARY KEY (DNI_CLIENTE),
	CONSTRAINT CHK_NOMBRE_CLIENTE CHECK (NOMBRE_CLIENTE LIKE '%[a-zA-Z]%'),
	CONSTRAINT CHK_MEMBRESIA CHECK (MEMBRESIA IN ('Bronce', 'Plata', 'Oro', 'Platino', 'Titanio'))
);

CREATE TABLE DEPOSITO(
	ID_DEPOSITO INT IDENTITY,
	NOMBRE_DEPOSITO VARCHAR(50) NOT NULL,
	CAPACIDAD INT NOT NULL,
	PROVINCIA VARCHAR(50) NOT NULL

	CONSTRAINT PK_ID_DEPOSITO PRIMARY KEY (ID_DEPOSITO),
	CONSTRAINT CHK_CAPACIDAD_DEPOSITO CHECK (CAPACIDAD >= 0),
	CONSTRAINT CHK_PROVINCIA_DEPOSITO CHECK (PROVINCIA LIKE '%[a-zA-Z]%')
);

CREATE TABLE PEDIDO(
	ID_PEDIDO INT IDENTITY,
	DNI_CLIENTE INT,
	FECHA_CREACION DATETIME NOT NULL,
	TOTAL FLOAT NOT NULL

	CONSTRAINT PK_ID_PEDIDO PRIMARY KEY (ID_PEDIDO),
    CONSTRAINT FK_DNI_CLIENTE_PEDIDO FOREIGN KEY (DNI_CLIENTE) REFERENCES CLIENTE(DNI_CLIENTE),
	CONSTRAINT CHK_TOTAL CHECK (TOTAL >= 0)
);

CREATE TABLE DETALLE_PEDIDO(
	ID_DETALLES_PEDIDO INT IDENTITY,
	ID_PEDIDO INT,
	CANTIDAD INT NOT NULL,
	CODIGO_PRODUCTO INT

	CONSTRAINT PK_ID_DETALLE_PEDIDO PRIMARY KEY (ID_DETALLES_PEDIDO),
	CONSTRAINT FK_ID_PEDIDO_DETALLE FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO),
	CONSTRAINT FK_CODIGO_PRODUCTO_DETALLE FOREIGN KEY (CODIGO_PRODUCTO) REFERENCES PRODUCTO(CODIGO_PRODUCTO),
	CONSTRAINT CHK_CANTIDAD CHECK (CANTIDAD >= 0)
);

CREATE TABLE VEHICULO(
	ID_VEHICULO INT IDENTITY,
	TIPO_VEHICULO VARCHAR(50) NOT NULL,
	CAPACIDAD INT NOT NULL

	CONSTRAINT PK_ID_VEHICULO PRIMARY KEY (ID_VEHICULO),
	CONSTRAINT CHK_TIPO_VEHICULO CHECK (TIPO_VEHICULO IN ('Camioneta', 'Camion', 'Camion Contenedor')),
	CONSTRAINT CHK_CAPACIDAD_VEHICULO CHECK (CAPACIDAD >= 0)

);

CREATE TABLE ENVIO(
	ID_ENVIO INT IDENTITY,
	CODIGO_ENVIO INT NOT NULL,
	CUIT_EMPLEADO BIGINT,
	ID_PEDIDO INT,
	ID_VEHICULO INT,
	FECHA_ENVIO DATE NOT NULL

	CONSTRAINT PK_ID_ENVIO PRIMARY KEY (ID_ENVIO),
	CONSTRAINT FK_ID_PEDIDO_ENVIO FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO),
	CONSTRAINT FK_CUIT_EMPLEADO_ENVIO FOREIGN KEY (CUIT_EMPLEADO) REFERENCES EMPLEADO(CUIT_EMPLEADO),
	CONSTRAINT FK_ID_VEHICULO_ENVIO FOREIGN KEY (ID_VEHICULO) REFERENCES VEHICULO(ID_VEHICULO),
	CONSTRAINT CHK_CODIGO_ENVIO CHECK (CODIGO_ENVIO >= 0)
);

