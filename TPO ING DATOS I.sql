CREATE PROCEDURE ACTUALIZAR_PRECIO_PRODUCTO
	@CODIGO_PRODUCTO INT,
	@PRECIO_NUEVO FLOAT
AS BEGIN
IF @CODIGO_PRODUCTO IN (SELECT CODIGO_PRODUCTO FROM PRODUCTO)
	UPDATE PRODUCTO
	SET PRECIO_PRODUCTO = @PRECIO_NUEVO
	WHERE CODIGO_PRODUCTO = @CODIGO_PRODUCTO
END

GO
CREATE PROCEDURE ACTUALIZAR_STOCK_PRODUCTO
	@CODIGO_PRODUCTO INT,
	@NUEVO_STOCK INT
AS BEGIN
IF @CODIGO_PRODUCTO IN (SELECT CODIGO_PRODUCTO FROM PRODUCTO)
	UPDATE PRODUCTO
	SET STOCK = @NUEVO_STOCK
	WHERE CODIGO_PRODUCTO = @CODIGO_PRODUCTO
END

GO 
CREATE PROCEDURE ACTUALIZAR_PROVINCIA_PROVEEDOR
    @CUIT_PROVEEDOR BIGINT,
    @UBICACION_SUCURSAL VARCHAR(50)
AS BEGIN
IF @CUIT_PROVEEDOR IN (SELECT CUIT_PROVEEDOR FROM PROVEEDOR)
    UPDATE PROVEEDOR SET PROVINCIA = @UBICACION_SUCURSAL WHERE @CUIT_PROVEEDOR = CUIT_PROVEEDOR
END

GO
CREATE PROCEDURE ACTUALIZAR_PUESTO_EMPLEADO
    @CUIT_EMPLEADO BIGINT,
    @NUEVO_PUESTO VARCHAR(50)
AS BEGIN
IF @CUIT_EMPLEADO IN (SELECT CUIT_EMPLEADO FROM EMPLEADO)
    UPDATE EMPLEADO SET PUESTO = @NUEVO_PUESTO WHERE CUIT_EMPLEADO = @CUIT_EMPLEADO
END

GO
CREATE PROCEDURE NUEVA_MEMBRESIA_CLIENTE
    @DNI_CLIENTE INT,
    @NUEVA_MEMBRESIA VARCHAR(50)
AS BEGIN
IF @DNI_CLIENTE IN (SELECT DNI_CLIENTE FROM CLIENTE)
    UPDATE CLIENTE SET MEMBRESIA = @NUEVA_MEMBRESIA WHERE DNI_CLIENTE = @DNI_CLIENTE
END

GO
CREATE PROCEDURE NUEVA_CAPACIDAD_DEPOSITO
    @ID_DEPOSITO INT,
    @NUEVA_CAPACIDAD INT
AS BEGIN
IF @ID_DEPOSITO IN (SELECT ID_DEPOSITO FROM DEPOSITO)
    UPDATE DEPOSITO SET CAPACIDAD = @NUEVA_CAPACIDAD WHERE ID_DEPOSITO = @ID_DEPOSITO
END

GO
CREATE PROCEDURE CAMBIAR_VEHICULO_ENVIO
    @ID_ENVIO INT,
    @ID_VEHICULO INT
AS BEGIN
IF (@ID_ENVIO IN (SELECT ID_ENVIO FROM ENVIO)) AND (@ID_VEHICULO IN (SELECT ID_VEHICULO FROM VEHICULO))
    UPDATE ENVIO SET @ID_VEHICULO = @ID_VEHICULO WHERE ID_ENVIO = @ID_ENVIO
END

