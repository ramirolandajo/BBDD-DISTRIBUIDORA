CREATE PROCEDURE CREAR_DETALLE_PEDIDO
	@ID_PEDIDO INT,
	@CANTIDAD INT,
	@CODIGO_PRODUCTO INT
AS BEGIN
IF @CANTIDAD <= (SELECT STOCK FROM PRODUCTO WHERE CODIGO_PRODUCTO = @CODIGO_PRODUCTO)
	INSERT INTO DETALLE_PEDIDO(ID_PEDIDO, CANTIDAD, CODIGO_PRODUCTO)
	VALUES (@ID_PEDIDO, @CANTIDAD, @CODIGO_PRODUCTO)

	UPDATE PRODUCTO
	SET STOCK = STOCK - @CANTIDAD
	WHERE CODIGO_PRODUCTO = @CODIGO_PRODUCTO;
END


